// 随机数字
export function randomNum(m: number, n: number) {
    return Math.floor(Math.random() * (n - m + 1) + m);
}

// 随机颜色
export function randomColor() {
    return `rgb(${randomNum(0, 255)}, ${randomNum(0, 255)}, ${randomNum(
        0,
        255
    )})`;
}

